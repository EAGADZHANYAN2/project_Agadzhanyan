#2. Описать функцию Mean(параметры), вычисляющую среднее арифметическое
#AMean = (X+Y)/2 и среднее геометрическое GMean = y/X Y двух положительных
#чисел X и Y. С помощью этой функции найти среднее арифметическое и среднее
#геометрическое для пар (A, B), (A, C), (A, D), если даны A, B, C, D.
# Описать функцию Mean, вычисляющую среднее арифметическое и среднее геометрическое

def Mean(X, Y):
    AMean = (X + Y) / 2
    GMean = (X * Y) ** 2
    return AMean, GMean

A = float(input("Введите число A: "))
B = float(input("Введите число B: "))
C = float(input("Введите число C: "))
D = float(input("Введите число D: "))

print("\nРезультаты:")
AMean_AB, GMean_AB = Mean(A, B)
print(f"Для пары (A,B): Среднее арифметическое = {AMean_AB}, Среднее геометрическое = {GMean_AB}")

AMean_AC, GMean_AC = Mean(A, C)
print(f"Для пары (A,C): Среднее арифметическое = {AMean_AC}, Среднее геометрическое = {GMean_AC}")

AMean_AD, GMean_AD = Mean(A, D)
print(f"Для пары (A,D): Среднее арифметическое = {AMean_AD}, Среднее геометрическое = {GMean_AD}")